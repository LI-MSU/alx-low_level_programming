#include <stdlib.h>

#include <stdio.h>

#include <time.h>

/**

 * main - Entry point

 *

 * Return: Always 0 (Success)

 */

int main(void)
  
{
  
  char alphabet;
  
  char bet_upper;
  

  
  for(alphabet = 'a';alphabet <= 'z';alphabet++)
    
    {
      
      putchar(alphabet);
      
    }
  
  for(bet_upper = 'A';bet_upper <= 'Z';bet_upper++)
    
    {
      
      putchar(bet_upper);
      
    }
  
  putchar('\n');
  
  return (0);
  
}
